CREATE TABLE BLOQUE(
IDBLOQUE       VARCHAR2(6) NOT NULL,
PRIMARY KEY (IDBLOQUE)
);

CREATE TABLE SALON(
IDSALON       VARCHAR2(6) NOT NULL,
PRIMARY KEY (IDSALON)
);

CREATE TABLE ASIGNATURA(
IDASIGNATURA       VARCHAR2(6) NOT NULL,
NOMBREASIGNATURA       VARCHAR2(50) NOT NULL,
INTENSIDADASIGNATURA       VARCHAR2(6) NOT NULL,
PRIMARY KEY (IDASIGNATURA)
);

CREATE TABLE ESTUDIANTE(
IDESTUDIANTE       VARCHAR2(13) NOT NULL,
NOMBREESTUDIANTE       VARCHAR2(50) NOT NULL,
APELLIDOESTUDIANTE       VARCHAR2(50) NOT NULL,
PRIMARY KEY (IDESTUDIANTE)
);

DROP TABLE PARCIAL;

CREATE TABLE PARCIAL(
IDPARCIAL        VARCHAR2(13) NOT NULL,
ASIGNATURAPARCIAL    VARCHAR2(6) NOT NULL,
ESTUDIENTEPARCIAL            VARCHAR2(13) NOT NULL,
BLOQUEPARCIAL          VARCHAR2(6) NOT NULL,
SALONPARCIAL         VARCHAR2(6) NOT NULL,
FECHA         DATE NOT NULL,
NOTA             VARCHAR2(70),
PRIMARY KEY (IDPARCIAL),
FOREIGN KEY (ASIGNATURAPARCIAL) REFERENCES ASIGNATURA,
FOREIGN KEY (ESTUDIENTEPARCIAL) REFERENCES ESTUDIANTE,
FOREIGN KEY (BLOQUEPARCIAL) REFERENCES BLOQUE,
FOREIGN KEY (SALONPARCIAL) REFERENCES SALON
);

